def list_chmi_timestamps():
    links = parse_listing_links(CHMI_MAXZ_BASE, verify=True)
    out = []
    for href in links:
        m = re.search(r"T_PABV23_C_OKPR_(\d{14})\.hdf", href)
        if m:
            out.append(m.group(1))
    return sorted(set(out))


def list_dwd_timestamps():
    links = parse_listing_links(DWD_DMAX_BASE, verify=True)
    out = []
    for href in links:
        m = re.search(r"composite_dmax_(\d{8})_(\d{4})-hd5", href)
        if m:
            out.append(m.group(1) + m.group(2) + "00")
    return sorted(set(out))


def list_shmu_timestamps_for_date(date_yyyymmdd):
    url = f"{SHMU_ZMAX_BASE}{date_yyyymmdd}/"
    try:
        links = parse_listing_links(url, verify=False)
    except Exception as e:
        print(f"SHMU listing failed for {date_yyyymmdd}: {e}")
        return []

    out = []
    for href in links:
        m = re.search(r"T_PABV22_C_LZIB_(\d{14})\.hdf", href)
        if m:
            out.append(m.group(1))
    return sorted(set(out))


def list_hu_files():
    """
    Hungarian MET refl2D NetCDF directory.
    This uses a broad parser because file naming may differ.
    Returns dict: timestamp -> href
    """
    try:
        links = parse_listing_links(HU_REFL2D_BASE, verify=False)
    except Exception as e:
        print(f"Hungarian MET listing failed: {e}")
        return {}

    out = {}
    for href in links:
        low = href.lower()
        if not (low.endswith(".nc") or low.endswith(".nc4") or low.endswith(".cdf") or ".nc?" in low or "refl" in low):
            continue
        ts = extract_timestamp_from_any_filename(href)
        if ts:
            out[ts] = href
    return dict(sorted(out.items()))


def find_latest_frame_set():
    cz_times = list_chmi_timestamps()
    de_times = list_dwd_timestamps()
    hu_files = list_hu_files()

    if not cz_times:
        raise RuntimeError("No CHMI timestamps found.")
    if not de_times:
        raise RuntimeError("No DWD timestamps found.")

    for cz_ts in reversed(cz_times[-80:]):
        date = cz_ts[:8]
        sk_times = list_shmu_timestamps_for_date(date)
        sk_ts = nearest_timestamp(cz_ts, sk_times)
        de_ts = nearest_timestamp(cz_ts, de_times)
        hu_ts = nearest_timestamp(cz_ts, list(hu_files.keys())) if hu_files else None

        if sk_ts and de_ts and (hu_ts or HU_OPTIONAL):
            return {
                "main_ts": cz_ts,
                "cz_ts": cz_ts,
                "sk_ts": sk_ts,
                "de_ts": de_ts,
                "hu_ts": hu_ts,
                "hu_href": hu_files.get(hu_ts) if hu_ts else None,
                "iso": ts_to_iso(cz_ts)
            }

    raise RuntimeError("Could not find matching CZ/SK/DE/HU radar times.")


def source_url(kind, ts, href=None):
    date = ts[:8]
    if kind == "cz":
        fn = f"T_PABV23_C_OKPR_{ts}.hdf"
        return CHMI_MAXZ_BASE + fn, fn
    if kind == "sk":
        fn = f"T_PABV22_C_LZIB_{ts}.hdf"
        return f"{SHMU_ZMAX_BASE}{date}/{fn}", fn
    if kind == "de":
        fn = f"composite_dmax_{ts[:8]}_{ts[8:12]}-hd5"
        return DWD_DMAX_BASE + fn, fn
    if kind == "hu":
        if not href:
            raise ValueError("HU source needs href from listing.")
        url = urljoin(HU_REFL2D_BASE, href)
        fn = href.rstrip("/").split("/")[-1]
        return url, fn
    raise ValueError(kind)


def download_source(kind, ts, href=None):
    url, fn = source_url(kind, ts, href=href)
    verify = False if kind in ("sk", "hu") else True
    return http_download(url, CACHE_DIR / fn, verify=verify)


