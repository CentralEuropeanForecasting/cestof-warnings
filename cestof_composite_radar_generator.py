def http_get_text(url, verify=True):
    r = requests.get(url, timeout=45, headers={"User-Agent": USER_AGENT}, verify=verify)
    r.raise_for_status()
    # Some directory listings have weird encodings; replace invalid characters.
    r.encoding = r.encoding or "utf-8"
    return r.text


def http_download(url, out_path, verify=True):
    out_path.parent.mkdir(parents=True, exist_ok=True)
    if out_path.exists() and out_path.stat().st_size > 0:
        return out_path

    print(f"Downloading: {url}")
    with requests.get(url, timeout=120, headers={"User-Agent": USER_AGENT}, verify=verify, stream=True) as r:
        r.raise_for_status()
        tmp = out_path.with_suffix(out_path.suffix + ".part")
        with open(tmp, "wb") as f:
            for chunk in r.iter_content(chunk_size=1024 * 1024):
                if chunk:
                    f.write(chunk)
        tmp.replace(out_path)

    return out_path


def parse_listing_links(url, verify=True):
    html = http_get_text(url, verify=verify)
    soup = BeautifulSoup(html, "html.parser")
    links = []
    for a in soup.find_all("a"):
        href = a.get("href")
        if href:
            links.append(href)
    return links

def ts_to_iso(ts):
    dt = datetime.strptime(ts, "%Y%m%d%H%M%S").replace(tzinfo=timezone.utc)
    return dt.isoformat().replace("+00:00", "Z")


def ts_to_epoch(ts):
    dt = datetime.strptime(ts, "%Y%m%d%H%M%S").replace(tzinfo=timezone.utc)
    return int(dt.timestamp())


def nearest_timestamp(target_ts, available_ts, max_diff_seconds=MAX_TIME_DIFF_SECONDS):
    if not available_ts:
        return None
    target = ts_to_epoch(target_ts)
    best = min(available_ts, key=lambda t: abs(ts_to_epoch(t) - target))
    if abs(ts_to_epoch(best) - target) <= max_diff_seconds:
        return best
    return None


def extract_timestamp_from_any_filename(name):
    """
    Accept many patterns:
    20260510175000
    20260510_1750
    2026-05-10_17-50
    """
    s = str(name)

    m = re.search(r"(20\d{6})(\d{6})", s)
    if m:
        return m.group(1) + m.group(2)

    m = re.search(r"(20\d{6})[_-]?(\d{4})(?!\d)", s)
    if m:
        return m.group(1) + m.group(2) + "00"

    m = re.search(r"(20\d{2})[-_]?(\d{2})[-_]?(\d{2}).*?(\d{2})[-_]?(\d{2})(?:[-_]?(\d{2}))?", s)
    if m:
        return "".join(m.groups(default="00"))

    return None


