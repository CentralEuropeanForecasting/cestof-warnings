def process_frame(frame_set):
    main_ts = frame_set["main_ts"]
    print(f"\nProcessing radar frame {main_ts}")
    print(frame_set)

    frame_png = FRAME_DIR / f"radar_{main_ts}.png"
    bounds_file = FRAME_DIR / f"radar_{main_ts}.bounds.json"
    merged_tif = FRAME_DIR / f"radar_{main_ts}_merged_3857.tif"

    if frame_png.exists() and bounds_file.exists():
        print("Frame already processed.")
        with open(bounds_file, "r", encoding="utf-8") as f:
            bounds = json.load(f)["bounds"]
        return {
            "timestamp": main_ts,
            "iso": frame_set["iso"],
            "png": f"radar_frames/radar_{main_ts}.png",
            "bounds": bounds,
            "sources": frame_set
        }

    source_specs = [
        ("cz", frame_set["cz_ts"], None),
        ("sk", frame_set["sk_ts"], None),
        ("de", frame_set["de_ts"], None),
    ]

    if frame_set.get("hu_ts") and frame_set.get("hu_href"):
        source_specs.append(("hu", frame_set["hu_ts"], frame_set["hu_href"]))
    else:
        print("Hungarian frame not available for this timestamp; continuing with CZ/SK/DE.")

    temp_dir = FRAME_DIR / f"_tmp_{main_ts}"
    if temp_dir.exists():
        shutil.rmtree(temp_dir)
    temp_dir.mkdir(parents=True, exist_ok=True)

    try:
        tifs = []
        for kind, ts, href in source_specs:
            try:
                file_path = download_source(kind, ts, href=href)
                arr, crs, transform = read_any_radar_raster(file_path)
                out_tif = temp_dir / f"{kind}_{main_ts}_3857.tif"
                write_reprojected(arr, crs, transform, out_tif, TARGET_EPSG, TARGET_RESOLUTION_METERS)
                tifs.append(out_tif)
                print(f"Added {kind.upper()} radar.")
            except Exception as e:
                if kind == "hu" and HU_OPTIONAL:
                    print(f"Hungarian radar failed, continuing without it: {e}")
                    continue
                raise

        merge_by_max(tifs, merged_tif)
        info = save_transparent_png_and_bounds(merged_tif, frame_png, bounds_file)

    finally:
        shutil.rmtree(temp_dir, ignore_errors=True)

    return {
        "timestamp": main_ts,
        "iso": frame_set["iso"],
        "png": f"radar_frames/radar_{main_ts}.png",
        "bounds": info["bounds"],
        "sources": frame_set
    }


def load_index():
    index_file = OUT_DIR / "radar_index.json"
    if not index_file.exists():
        return {"updated_at": None, "frames": []}
    try:
        return json.loads(index_file.read_text(encoding="utf-8"))
    except Exception:
        return {"updated_at": None, "frames": []}


def save_index(index):
    index["updated_at"] = datetime.now(timezone.utc).isoformat().replace("+00:00", "Z")
    index_file = OUT_DIR / "radar_index.json"
    tmp = index_file.with_suffix(".json.part")
    tmp.write_text(json.dumps(index, indent=2), encoding="utf-8")
    tmp.replace(index_file)


def update_once():
    ensure_dirs()
    frame_set = find_latest_frame_set()
    frame = process_frame(frame_set)

    index = load_index()
    frames = index.get("frames", [])

    frames = [f for f in frames if f.get("timestamp") != frame["timestamp"]]
    frames.append(frame)
    frames = sorted(frames, key=lambda f: f["timestamp"])[-MAX_FRAMES_ON_HTML_SLIDER:]

    index["frames"] = frames
    save_index(index)

    print(f"Composite index updated with {len(frames)} frame(s). Latest: {frame['timestamp']}")

def loop():
    ensure_dirs()
    print("CESTOF composite radar CZ/SK/DE/HU started.")
    print(f"Refresh interval: {INTERVAL_SECONDS} seconds")
    print(f"Output folder: {OUT_DIR.resolve()}")

    while True:
        try:
            update_once()
        except Exception as e:
            print("ERROR:", repr(e))
        time.sleep(INTERVAL_SECONDS)


if __name__ == "__main__":
    import os
    if os.environ.get("RUN_ONCE", "").strip().lower() in ("1", "true", "yes", "y"):
        update_once()
    else:
        loop()
