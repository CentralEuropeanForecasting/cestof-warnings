    ax.imshow(masked, cmap=cmap, norm=norm, interpolation="nearest", origin="upper")
    ax.set_axis_off()
    fig.savefig(out_png, transparent=True, pad_inches=0)
    plt.close(fig)

    info = {
        "bounds": [[south, west], [north, east]],
        "west": west,
        "south": south,
        "east": east,
        "north": north
    }
    with open(bounds_json, "w", encoding="utf-8") as f:
        json.dump(info, f, indent=2)

    return info


