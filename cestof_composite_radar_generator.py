def _decode_attr(v):
    if isinstance(v, bytes):
        return v.decode("utf-8", errors="ignore")
    if isinstance(v, np.ndarray) and v.shape == ():
        return v.item()
    if isinstance(v, np.generic):
        return v.item()
    return v


def attrs_dict(obj):
    return {k: _decode_attr(v) for k, v in obj.attrs.items()}


def find_first_2d_dataset(h5):
    preferred = [
        "/dataset1/data1/data",
        "dataset1/data1/data",
        "/dataset1/data2/data",
        "dataset1/data2/data",
    ]
    for p in preferred:
        if p in h5 and isinstance(h5[p], h5py.Dataset) and h5[p].ndim == 2:
            return p

    candidates = []
    def visitor(name, obj):
        if isinstance(obj, h5py.Dataset) and obj.ndim == 2:
            score = 0
            if name.lower().endswith("/data") or name.lower() == "data":
                score += 10
            if "dataset1" in name.lower():
                score += 3
            if "data1" in name.lower():
                score += 2
            candidates.append((score, name))
    h5.visititems(visitor)

    if not candidates:
        raise RuntimeError("No 2D dataset found in HDF5 file.")
    candidates.sort(reverse=True)
    return candidates[0][1]


def get_group_attrs_upwards(h5, dataset_path):
    groups = ["/", "/what", "/where", "/how"]

    parts = dataset_path.strip("/").split("/")
    if len(parts) >= 1:
        groups += ["/" + parts[0], "/" + parts[0] + "/what", "/" + parts[0] + "/where", "/" + parts[0] + "/how"]
    if len(parts) >= 2:
        g = "/" + "/".join(parts[:2])
        groups += [g, g + "/what", g + "/where", g + "/how"]

    meta = {}
    for g in groups:
        if g in h5:
            for k, v in attrs_dict(h5[g]).items():
                meta[k] = v

    for k, v in attrs_dict(h5[dataset_path]).items():
        meta[k] = v

    return meta


def read_odim_hdf_raster(path):
    path = Path(path)

    with h5py.File(path, "r") as h5:
        dset_path = find_first_2d_dataset(h5)
        raw = h5[dset_path][:]
        meta = get_group_attrs_upwards(h5, dset_path)

        data_group = "/" + "/".join(dset_path.strip("/").split("/")[:-1])
        data_meta = {}
        if data_group in h5:
            data_meta.update(attrs_dict(h5[data_group]))
        if data_group + "/what" in h5:
            data_meta.update(attrs_dict(h5[data_group + "/what"]))

        gain = float(data_meta.get("gain", meta.get("gain", 1.0)))
        offset = float(data_meta.get("offset", meta.get("offset", 0.0)))
        nodata = data_meta.get("nodata", meta.get("nodata", None))
        undetect = data_meta.get("undetect", meta.get("undetect", None))

        arr = raw.astype("float32")
        mask = np.zeros(arr.shape, dtype=bool)

        if nodata is not None:
            try:
                mask |= raw == float(nodata)
            except Exception:
                pass
        if undetect is not None:
            try:
                mask |= raw == float(undetect)
            except Exception:
                pass

        arr = arr * gain + offset
        arr[mask] = np.nan

        projdef = meta.get("projdef", None)
        if projdef:
            src_crs = CRS.from_string(str(projdef))
            py_src = PyprojCRS.from_user_input(str(projdef))
        else:
            warnings.warn(f"No projdef found in {path.name}. Falling back to EPSG:4326.")
            src_crs = CRS.from_epsg(4326)
            py_src = PyprojCRS.from_epsg(4326)

        corner_keys = ["LL_lon", "LL_lat", "UL_lon", "UL_lat", "UR_lon", "UR_lat", "LR_lon", "LR_lat"]
        has_corners = all(k in meta for k in corner_keys)

        height, width = arr.shape

        if has_corners:
            ll = (float(meta["LL_lon"]), float(meta["LL_lat"]))
            ul = (float(meta["UL_lon"]), float(meta["UL_lat"]))
            ur = (float(meta["UR_lon"]), float(meta["UR_lat"]))
            lr = (float(meta["LR_lon"]), float(meta["LR_lat"]))

            transformer = Transformer.from_crs("EPSG:4326", py_src, always_xy=True)
            xs, ys = transformer.transform(
                [ll[0], ul[0], ur[0], lr[0]],
                [ll[1], ul[1], ur[1], lr[1]],
            )

            left = min(xs)
            right = max(xs)
            bottom = min(ys)
            top = max(ys)

            px_w = (right - left) / width
            px_h = (top - bottom) / height
            transform = Affine(px_w, 0, left, 0, -px_h, top)

        else:
            xscale = float(meta.get("xscale", meta.get("XScale", 1000.0)))
            yscale = float(meta.get("yscale", meta.get("YScale", 1000.0)))

            if "LL_lon" in meta and "LL_lat" in meta:
                transformer = Transformer.from_crs("EPSG:4326", py_src, always_xy=True)
                left, bottom = transformer.transform(float(meta["LL_lon"]), float(meta["LL_lat"]))
                top = bottom + height * yscale
                transform = Affine(xscale, 0, left, 0, -yscale, top)
            else:
                raise RuntimeError(f"Could not georeference {path.name}. Missing corner metadata.")

        return arr, src_crs, transform


def read_hu_netcdf_raster(path):
    """
    Read Hungarian MET refl2D NetCDF.
    This tries common NetCDF layouts:
    - data variable with lon/lat 1D coordinates
    - data variable with 2D lon/lat coordinates
    - projected x/y coordinates with CRS attrs
    """
    try:
        import xarray as xr
    except Exception as e:
        raise RuntimeError("Hungarian NetCDF support needs xarray + netCDF4: pip install xarray netCDF4") from e

    ds = xr.open_dataset(path)

    # Pick likely reflectivity variable.
    preferred_names = ["refl2D", "refl", "reflectivity", "DBZH", "dbzh", "data", "radar"]
    data_var = None

    for name in preferred_names:
        if name in ds.data_vars:
            data_var = ds[name]
            break

    if data_var is None:
        candidates = []
        for name, da in ds.data_vars.items():
            if len(da.shape) >= 2 and np.issubdtype(da.dtype, np.number):
                score = 0
                low = name.lower()
                if "refl" in low or "dbz" in low or "dbzh" in low:
                    score += 10
                candidates.append((score, name, da))
        if not candidates:
            raise RuntimeError(f"No usable 2D numeric NetCDF variable found in {path}")
        candidates.sort(reverse=True)
        data_var = candidates[0][2]

    # Remove time/level dimensions by taking latest/first.
    da = data_var
    while da.ndim > 2:
        da = da.isel({da.dims[0]: -1})

    arr = np.array(da.values, dtype="float32")

    # Apply scale/offset if xarray did not decode for some reason.
    fill_values = []
    for key in ["_FillValue", "missing_value"]:
        if key in data_var.attrs:
            try:
                fill_values.append(float(data_var.attrs[key]))
            except Exception:
                pass
    for fv in fill_values:
        arr[arr == fv] = np.nan

    # Try to find lon/lat coordinate names.
    lon_names = ["lon", "longitude", "xlon", "longitudes"]
    lat_names = ["lat", "latitude", "xlat", "latitudes"]

    lon = None
    lat = None
    for n in lon_names:
        if n in ds.coords:
            lon = ds[n]
            break
        if n in ds:
            lon = ds[n]
            break
    for n in lat_names:
        if n in ds.coords:
            lat = ds[n]
            break
        if n in ds:
            lat = ds[n]
            break

    if lon is not None and lat is not None:
        lonv = np.array(lon.values)
        latv = np.array(lat.values)

        # 1D regular lon/lat grid.
        if lonv.ndim == 1 and latv.ndim == 1:
            # Make sure y order matches array.
            west = float(np.nanmin(lonv))
            east = float(np.nanmax(lonv))
            south = float(np.nanmin(latv))
            north = float(np.nanmax(latv))

            h, w = arr.shape
            transform = Affine((east - west) / w, 0, west, 0, -(north - south) / h, north)
            return arr, CRS.from_epsg(4326), transform

        # 2D lon/lat grid; approximate as rectified bounds.
        if lonv.ndim == 2 and latv.ndim == 2:
            west = float(np.nanmin(lonv))
            east = float(np.nanmax(lonv))
            south = float(np.nanmin(latv))
            north = float(np.nanmax(latv))

            h, w = arr.shape
            transform = Affine((east - west) / w, 0, west, 0, -(north - south) / h, north)
            return arr, CRS.from_epsg(4326), transform

    # Try projected x/y coords.
    x = None
    y = None
    for n in ["x", "X", "easting", "projection_x_coordinate"]:
        if n in ds.coords:
            x = ds[n]
            break
    for n in ["y", "Y", "northing", "projection_y_coordinate"]:
        if n in ds.coords:
            y = ds[n]
            break

    if x is not None and y is not None:
        xv = np.array(x.values, dtype="float64")
        yv = np.array(y.values, dtype="float64")
        west = float(np.nanmin(xv))
        east = float(np.nanmax(xv))
        south = float(np.nanmin(yv))
        north = float(np.nanmax(yv))

        h, w = arr.shape
        transform = Affine((east - west) / w, 0, west, 0, -(north - south) / h, north)

        # Try CRS from common attrs; fallback to EPSG:4326 is bad but explicit.
        crs = None
        for var in ds.variables.values():
            attrs = getattr(var, "attrs", {})
            for key in ["spatial_ref", "crs_wkt"]:
                if key in attrs:
                    try:
                        crs = CRS.from_string(attrs[key])
                    except Exception:
                        pass
        if crs is None:
            warnings.warn(f"Could not find CRS in {path.name}; assuming EPSG:4326.")
            crs = CRS.from_epsg(4326)

        return arr, crs, transform

    raise RuntimeError(f"Could not georeference Hungarian NetCDF file: {path}")


def read_any_radar_raster(path):
    path = Path(path)
    low = path.name.lower()
    if low.endswith(".nc") or low.endswith(".nc4") or low.endswith(".cdf"):
        return read_hu_netcdf_raster(path)
    return read_odim_hdf_raster(path)


