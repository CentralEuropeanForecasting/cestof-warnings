def clean_radar_array(arr):
    arr = arr.astype("float32", copy=True)
    arr[arr > 80] = np.nan
    arr[arr < 0] = np.nan
    return arr


def radar_colormap():
    bounds = [-10, 0, 5, 10, 15, 20, 25, 30, 35, 40, 45, 50, 55, 60, 65, 75]
    colors = [
        "#00000000",
        "#001a66", "#004dff", "#00bfff",
        "#00cc66", "#33ff33",
        "#ffff00", "#ffcc00", "#ff9900",
        "#ff0000", "#cc0000",
        "#9900cc", "#ff00ff",
        "#ffffff", "#ffccff",
    ]
    return ListedColormap(colors), BoundaryNorm(bounds, len(colors))


def write_reprojected(src_arr, src_crs, src_transform, out_tif, dst_epsg=3857, resolution=1000):
    src_arr = clean_radar_array(src_arr)

    height, width = src_arr.shape
    left, bottom, right, top = rasterio.transform.array_bounds(height, width, src_transform)

    dst_crs = CRS.from_epsg(dst_epsg)
    dst_transform, dst_width, dst_height = calculate_default_transform(
        src_crs, dst_crs, width, height, left, bottom, right, top, resolution=resolution
    )

    dst = np.full((dst_height, dst_width), np.nan, dtype="float32")

    reproject(
        source=src_arr,
        destination=dst,
        src_transform=src_transform,
        src_crs=src_crs,
        src_nodata=np.nan,
        dst_transform=dst_transform,
        dst_crs=dst_crs,
        dst_nodata=np.nan,
        resampling=Resampling.nearest,
    )

    profile = {
        "driver": "GTiff",
        "height": dst_height,
        "width": dst_width,
        "count": 1,
        "dtype": "float32",
        "crs": dst_crs,
        "transform": dst_transform,
        "nodata": np.nan,
        "compress": "deflate",
        "predictor": 2,
    }

    with rasterio.open(out_tif, "w", **profile) as ds:
        ds.write(dst, 1)

    return out_tif


def merge_by_max(tif_paths, out_tif):
    srcs = [rasterio.open(p) for p in tif_paths]
    try:
        mosaic, out_transform = merge(srcs, method="first", nodata=np.nan)
        base_profile = srcs[0].profile.copy()
        base_profile.update({
            "height": mosaic.shape[1],
            "width": mosaic.shape[2],
            "transform": out_transform,
            "nodata": np.nan,
            "compress": "deflate",
            "predictor": 2,
        })

        stack = []
        for src in srcs:
            tmp = np.full((mosaic.shape[1], mosaic.shape[2]), np.nan, dtype="float32")
            reproject(
                source=rasterio.band(src, 1),
                destination=tmp,
                src_transform=src.transform,
                src_crs=src.crs,
                src_nodata=np.nan,
                dst_transform=out_transform,
                dst_crs=srcs[0].crs,
                dst_nodata=np.nan,
                resampling=Resampling.nearest,
            )
            stack.append(tmp)

        merged = np.nanmax(np.stack(stack, axis=0), axis=0).astype("float32")

        with rasterio.open(out_tif, "w", **base_profile) as dst:
            dst.write(merged, 1)

    finally:
        for s in srcs:
            s.close()

    return out_tif


def save_transparent_png_and_bounds(tif_path, out_png, bounds_json):
    with rasterio.open(tif_path) as src:
        arr = clean_radar_array(src.read(1))
        b = src.bounds
        transformer = Transformer.from_crs(src.crs, "EPSG:4326", always_xy=True)
        west, south = transformer.transform(b.left, b.bottom)
        east, north = transformer.transform(b.right, b.top)

    cmap, norm = radar_colormap()
    masked = np.ma.masked_invalid(arr)

    # Exact raster aspect ratio. Keep pixels sharp and unblurred.
    h, w = arr.shape
    fig_w = max(8, min(16, w / 120))
    fig_h = fig_w * h / w
